public class _18_StringsInJava {
    public static void main(String[] args) {

        String name = "Jarvis";

        // To get all String methods just do name(dot) -> name.
        System.out.println(name.length());

        StringBuffer sb = new StringBuffer("Wuod");

        // To get all StringBuffer methods just do sb(dot) -> sb.
        System.out.println(sb.substring(1));
    }
}
