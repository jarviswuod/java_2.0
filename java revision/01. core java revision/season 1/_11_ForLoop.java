public class _11_ForLoop {

    public static void main(String[] args) {

        // Increament for loop
        for (int i = 1; i <= 10; i++) {
            System.out.println("Jarvis : " + i);
        }

        // Decreament for loop
        for (int i = 10; i > 0; i--) {
            System.out.println("Jarvis reversed : " + i);
        }
    }
}
